#!/usr/bin/env python3
"""
update-benchmarks.py CSV_FILE DOC_FILE

Read the anthocnet-compare CSV (as produced by run-comparison.sh) and rewrite
the results table inside DOC_FILE, between the markers:

    <!-- BENCHMARK-TABLE-START -->
    ... generated table ...
    <!-- BENCHMARK-TABLE-END -->

Used by the benchmark CI pipeline to keep docs/benchmarks.md current, and
runnable by hand. Deterministic: if the table is unchanged, the file is
unchanged (so CI does not create empty commits).
"""
import sys
import csv

START = "<!-- BENCHMARK-TABLE-START -->"
END = "<!-- BENCHMARK-TABLE-END -->"


_TABLE_HEAD = [
    "| protocol | PDR % | mean delay (ms) | 99th delay (ms) | throughput (kbps) | NRL "
    "| jitter (ms) | dOff90 (ms) |",
    "|----------|------:|----------------:|----------------:|------------------:|----:"
    "|------------:|------------:|",
]


def _off(v):
    # #57 offered-load percentile: -1 encodes infinity (less than the offered
    # fraction ever delivered).
    return "inf" if v not in (None, "", "-") and float(v) < 0 else (v or "-")


def _row(r):
    # Columns after delay_ms are newer; tolerate older CSVs without them.
    # jitter_ms / delay_off90_ms are the #57 paper-parity QoS metrics.
    return (f"| {r['protocol']} | {r['pdr_pct']} | {r['delay_ms']} | "
            f"{r.get('delay99_ms', '-')} | {r['throughput_kbps']} | "
            f"{r.get('nrl', '-')} | {r.get('jitter_ms', '-')} | "
            f"{_off(r.get('delay_off90_ms'))} |")


def build_single(rows):
    """Old format: one scenario, one table (anthocnet-compare CSV)."""
    meta = rows[0]
    header = (
        f"_Scenario: {meta['nNodes']} nodes, {meta['area']} m area, "
        f"max speed {meta['speed']} m/s, {meta['flows']} flows, "
        f"mean of {meta['runs']} run(s). Generated by `anthocnet-compare`._\n\n"
    )
    return header + "\n".join(_TABLE_HEAD + [_row(r) for r in rows]) + "\n"


def build_classified(rows):
    """Classified format (run-scenarios.py): one table per named scenario, plus
    the taxonomy chart embedded so the figures publish alongside the numbers."""
    discrete = [r for r in rows if r.get("kind") == "discrete"]
    if not discrete:
        raise SystemExit("classified CSV has no discrete rows")
    runs = discrete[0].get("runs", "?")
    out = [
        f"_Scenario taxonomy — mean of {runs} run(s) per scenario, every baseline "
        f"on identical realisations. Generated by `run-scenarios.py`; charts by "
        f"`make-charts.py`._\n",
        "![scenario taxonomy](benchmarks/discrete-summary.png)\n",
    ]
    seen = []
    for r in discrete:
        if r["scenario"] not in seen:
            seen.append(r["scenario"])
    for scen in seen:
        srows = [r for r in discrete if r["scenario"] == scen]
        klass = srows[0].get("class", "")
        out.append(f"\n#### {scen} — {klass}\n")
        out.append("\n".join(_TABLE_HEAD + [_row(r) for r in srows]))
        out.append("")
    # No timestamp: deterministic output (only changes when behaviour does).
    return "\n".join(out) + "\n"


def build_table(csv_path):
    with open(csv_path, newline="") as f:
        rows = list(csv.DictReader(f))
    if not rows:
        raise SystemExit("no CSV rows")
    if "kind" in rows[0] and "scenario" in rows[0]:
        return build_classified(rows)
    return build_single(rows)


def main():
    if len(sys.argv) != 3:
        raise SystemExit("usage: update-benchmarks.py CSV_FILE DOC_FILE")
    csv_path, doc_path = sys.argv[1], sys.argv[2]
    table = build_table(csv_path)

    with open(doc_path) as f:
        doc = f.read()
    if START not in doc or END not in doc:
        raise SystemExit(f"markers {START} / {END} not found in {doc_path}")

    pre = doc[: doc.index(START) + len(START)]
    post = doc[doc.index(END):]
    new_doc = pre + "\n" + table + "\n" + post

    if new_doc != doc:
        with open(doc_path, "w") as f:
            f.write(new_doc)
        print(f"updated {doc_path}")
    else:
        print(f"{doc_path} already up to date")


if __name__ == "__main__":
    main()
