#!/usr/bin/env python3
"""
update-benchmarks.py CSV_FILE DOC_FILE

Read the anthocnet-compare CSV (as produced by run-comparison.sh) and rewrite
the results table inside DOC_FILE, between the markers:

    <!-- BENCHMARK-TABLE-START -->
    ... generated table ...
    <!-- BENCHMARK-TABLE-END -->

Used by the benchmark CI pipeline to keep docs/benchmarks.md current, and
runnable by hand. Deterministic: if the table is unchanged, the file is
unchanged (so CI does not create empty commits).
"""
import sys
import csv

START = "<!-- BENCHMARK-TABLE-START -->"
END = "<!-- BENCHMARK-TABLE-END -->"


def build_table(csv_path):
    with open(csv_path, newline="") as f:
        rows = list(csv.DictReader(f))
    if not rows:
        raise SystemExit("no CSV rows")

    meta = rows[0]
    header = (
        f"_Scenario: {meta['nNodes']} nodes, {meta['area']} m area, "
        f"max speed {meta['speed']} m/s, {meta['flows']} flows, "
        f"mean of {meta['runs']} run(s). Generated by `anthocnet-compare`._\n\n"
    )
    lines = [
        "| protocol | PDR % | mean delay (ms) | 99th delay (ms) | throughput (kbps) | NRL |",
        "|----------|------:|----------------:|----------------:|------------------:|----:|",
    ]
    for r in rows:
        # delay99_ms / nrl are newer columns; tolerate older CSVs without them.
        d99 = r.get("delay99_ms", "-")
        nrl = r.get("nrl", "-")
        lines.append(
            f"| {r['protocol']} | {r['pdr_pct']} | {r['delay_ms']} | {d99} | "
            f"{r['throughput_kbps']} | {nrl} |"
        )
    # No timestamp: the seeds are fixed, so the table is deterministic and only
    # changes when the code's routing behaviour does (no noisy daily commits).
    return header + "\n".join(lines) + "\n"


def main():
    if len(sys.argv) != 3:
        raise SystemExit("usage: update-benchmarks.py CSV_FILE DOC_FILE")
    csv_path, doc_path = sys.argv[1], sys.argv[2]
    table = build_table(csv_path)

    with open(doc_path) as f:
        doc = f.read()
    if START not in doc or END not in doc:
        raise SystemExit(f"markers {START} / {END} not found in {doc_path}")

    pre = doc[: doc.index(START) + len(START)]
    post = doc[doc.index(END):]
    new_doc = pre + "\n" + table + "\n" + post

    if new_doc != doc:
        with open(doc_path, "w") as f:
            f.write(new_doc)
        print(f"updated {doc_path}")
    else:
        print(f"{doc_path} already up to date")


if __name__ == "__main__":
    main()
